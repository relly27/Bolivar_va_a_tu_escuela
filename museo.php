                        <p>
                        Simón José Antonio Bolívar Palacios nació en esta casa un 24 de Julio de 1783, en la actual Caracas, pero para el momento era la capital de la Capitanía General de Venezuela, provincia del imperio español.<br><br>

                        La Casa natal es una construcción que data del siglo XVII, propiedad de don Pedro de Ponte Andrade Jaspe de Montenegro por el matrimonio de su hija María Petronila Ponte Andrade Jaspe de Montenegro con don Juan Bolívar Villegas, hasta llegar a Don Juan Vicente Bolívar al contraer matrimonio el 1 de diciembre de 1773 con doña María de la Concepción Palacios Blanco, quienes trajeron al mundo a cuatro hijos en este inmueble: María Antonia, Juana, Juan Vicente y Simón.<br><br>

                        En 1786 fallece Don Juan Vicente y en 1792 Doña María Concepción, la casa es deshabitada y luego puesta en venta en 1806, a Don Juan de La Madrid, pariente del Libertador. En 1876 fue comprada por el General Guzmán Blanco, quienes junto a sus sucesores dieron a la propiedad diversos usos “fue alquilada a comerciantes de víveres y dividida en dos locales por una pared a lo largo del edificio”

                        El 11 de octubre de 1912, la casa fue comprada por el estado venezolano, mediante una comisión que se encargó de recoger fondos para tal fin. “El costo del inmueble, para entonces propiedad de la señora Ana Teresa Ibarra, viuda de Guzmán, fue de 114.326 bolívares, de los cuales 46.675,25 aportó el pueblo de Caracas”<br><br>

                        Luego, en 1916 se hace oficial el decreto del entonces presidente provisional de los estados Unidos de Venezuela V. Márquez Bustillos,(puesto por Juan Vicente Gómez), donde se promulgó la reconstrucción y embellecimiento de la casa, con una junta para tal fin: el historiador e ingeniero Vicente Lecuna, los arquitectos Antonio Malaussena y Alejandro Chataing, Luis Malaussena discípulo de la Academia de Bellas Artes de Paris, el bibliófilo Manuel Segundo Sánchez, el anticuario Christián Witzke, el investigador Manuel Landaeta Rosales y el pintor Tito Salas.<br><br>

                        Así el 5 de Julio de 1921, con el discurso del presbítero Carlos Borges, se realiza la ceremonia de apertura de la Casa Natal del Libertador, con el objetivo de glorificar la obra del hombre que dio la Libertad a los pueblos de América, “un santuario de veneración permanente al Libertador”, y eso se logra apreciar en la infraestructura y en los objetos contenidos en ella, que responden a la consideración del Padre de la Patria, sin embargo, permitieron a los venezolanos involucrarse en la confluencia nacional patriótica  y a ese pasado que nos proporciona identidad.<br><br>

                        La llegada y construcción de la reciprocidad afectiva entre el pasado histórico y nuestro pueblo es un esfuerzo de años, desde nuestra independencia hasta nuestros días, en el año 1999, la Casa Natal del Libertador, así como la colección de bienes muebles y enseres que alberga, fueron declarados Bienes de Interés Cultural y en el 2002, la edificación fue declarada Monumento Histórico Nacional.<br><br>

                        Este patrimonio gana valor en nuestra conciencia colectiva, por ello el esfuerzo en su preservación, cuidado, estudio y difusión, cuya tarea en pleno siglo XXI está en las manos de todos los venezolanos, desde cada rincón del país, la historia nos ha designado una actuación de valentía y perseverancia, tal como nuestros ancestros en el siglo XIX.<br><br>
